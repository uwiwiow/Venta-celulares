    private void actualizarEmpleadoTable() {
        EmpleadoDao dao = new EmpleadoDao();
        listaEmpleado = dao.listar();

        // Crear un modelo de tabla personalizado
        DefaultTableModel mt = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        };
        
        String ids[] = {"Numero de Empleado", "Nombre Completo", "Turno"};
        mt.setColumnIdentifiers(ids);
        empleadoTable.setModel(mt);

        // Configurar el renderizador de celdas para aumentar el espacio entre filas
        empleadoTable.setDefaultRenderer(Object.class, new CustomTableCellRenderer());
        empleadoTable.getTableHeader().setReorderingAllowed(false);

        for (int i = 0; i < listaEmpleado.size(); i++) {
            Empleado empleado = listaEmpleado.get(i);
            mt.addRow(new Object[]{empleado.getNumEmpleado(), empleado.getNombreCompleto(), empleado.getTurno()});
        }
    }
    
