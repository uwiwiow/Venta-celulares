    private void formatosVentaCBox() {
        
//        CBox Empleado
        EmpleadoDao Edao = new EmpleadoDao();
        List<Empleado> listaE = Edao.listar();
        
        Object indexE [] = new Object[listaE.size()];
        
        for (int i = 0; i < listaE.size(); i++) {
            Empleado empleado = listaE.get(i);
            indexE[i] = empleado.getNumEmpleado() + " " + empleado.getNombreCompleto();
        }
        
        ventaEmpleadoCBox.setModel(new DefaultComboBoxModel(indexE));
        
//        CBox Empleado
        ClienteDao Cdao = new ClienteDao();
        List<Cliente> listaC = Cdao.listar();
        
        Object indexC [] = new Object[listaC.size()];
        
        for (int i = 0; i < listaC.size(); i++) {
            Cliente cliente = listaC.get(i);
            indexC[i] = cliente.getNumCliente() + " " + cliente.getNombreCompleto();
        }
        
        ventaClienteCBox.setModel(new DefaultComboBoxModel(indexC));
        
//        CBox fecha
        LocalDate fechaActual = LocalDate.now();
        
        Object [] days = new Object[31];
        
        for (int i = 0; i < days.length; i++) {
            days[i] = i + 1;
        }
        
        ventaDiaCBox.setModel(new DefaultComboBoxModel(days));
        
        Object months [] = {"Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio", "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"};
        
        ventaMesCBox.setModel(new DefaultComboBoxModel(months));
        
        Object [] years = new Object[10];
        int j = 0;
        for (int i = fechaActual.getYear(); i >= (fechaActual.getYear() - 9); i--) {
            years[j] = i;
            j++;
        }
        
        ventaAnoCBox.setModel(new DefaultComboBoxModel(years));
        
        ventaDiaCBox.setSelectedIndex(fechaActual.getDayOfMonth() - 1);
        ventaMesCBox.setSelectedIndex(fechaActual.getMonthValue() - 1);
        ventaAnoCBox.setSelectedIndex(0);
        
    }
    
