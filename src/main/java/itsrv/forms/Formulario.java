    private int monthToInt(Object mes){
        Object months [] = {"Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio", "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"};
        
        for (int i = 0; i < months.length; i++) {
            if (mes == months[i]){
                return i + 1;
            }
        }
        return 0;
    }
    
    private void aceptarVentaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarVentaBtnActionPerformed
        
        Venta venta = new Venta();
        VentaDao vdao = new VentaDao();
        
        venta.setNoE(Integer.parseInt((String.valueOf(ventaEmpleadoCBox.getSelectedItem()).indexOf(' ') != -1) ? String.valueOf(ventaEmpleadoCBox.getSelectedItem()).substring(0, String.valueOf(ventaEmpleadoCBox.getSelectedItem()).indexOf(' ')) : String.valueOf(ventaEmpleadoCBox.getSelectedItem())));
        venta.setNoC(Integer.parseInt((String.valueOf(ventaClienteCBox.getSelectedItem()).indexOf(' ') != -1) ? String.valueOf(ventaClienteCBox.getSelectedItem()).substring(0, String.valueOf(ventaClienteCBox.getSelectedItem()).indexOf(' ')) : String.valueOf(ventaClienteCBox.getSelectedItem())));
        venta.setFecha(Date.valueOf(ventaAnoCBox.getSelectedItem() + "-" + monthToInt(ventaMesCBox.getSelectedItem()) + "-" + ventaDiaCBox.getSelectedItem()));
        venta.setFolioV(Integer.parseInt(folioVentaTxt.getText()));
        vdao.agregar(venta);
        
        DSV dsv = new DSV();
        DSVDao ddao = new DSVDao();
        float total = 0;
        
        for (CelularDSV cdsv: listaCelularDSV) {
            dsv.setFolioV(Integer.parseInt(folioVentaTxt.getText()));
            dsv.setNoC(cdsv.getNumCelular());
            dsv.setCantidad(cdsv.getCantidad());
            dsv.setImporte(cdsv.getImporte());
            total += dsv.getImporte();
            ddao.agregar(dsv);
        }
        
        importeVentaLbl.setText("$ " + String.valueOf(total));
        
    }//GEN-LAST:event_aceptarVentaBtnActionPerformed

